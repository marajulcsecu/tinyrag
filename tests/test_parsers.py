class TestParsedDocument:
    """The result dataclass behaves correctly."""

    def test_dataclass_is_frozen(self) -> None:
        doc = ParsedDocument(text="hello")
        with pytest.raises((AttributeError, Exception)):
            doc.text = "world"  # type: ignore[misc]

    def test_pages_default_is_empty(self) -> None:
        """Critical: defaults must NOT be mutable shared state."""
        d1 = ParsedDocument(text="a")
        d2 = ParsedDocument(text="b")
        assert d1.pages == []
        assert d2.pages == []
        d1.pages.append((1, "x"))  # should not affect d2
        assert d2.pages == []

    def test_metadata_default_is_empty(self) -> None:
        d1 = ParsedDocument(text="a")
        d2 = ParsedDocument(text="b")
        d1.metadata["k"] = "v"
        assert "k" not in d2.metadata


class TestExtensionDispatch:
    """:func:`parse` routes to the right parser by file extension."""

    def test_pdf_routes_to_pdf_parser(
        self, sample_pdf: Path
    ) -> None:
        doc = parse(sample_pdf)
        assert doc.metadata["format"] == "pdf"

    def test_txt_routes_to_txt_parser(
        self, sample_txt: Path
    ) -> None:
        doc = parse(sample_txt)
        assert doc.metadata["format"] == "txt"

    def test_md_routes_to_markdown_parser(
        self, sample_md: Path
    ) -> None:
        doc = parse(sample_md)
        assert doc.metadata["format"] == "md"

    def test_markdown_extension_also_works(
        self, sample_md: Path
    ) -> None:
        p = sample_md.with_suffix(".markdown")
        sample_md.rename(p)
        doc = parse(p)
        assert doc.metadata["format"] == "md"

    def test_uppercase_extension_is_case_insensitive(
        self, sample_txt: Path
    ) -> None:
        upper = sample_txt.with_suffix(".TXT")
        sample_txt.rename(upper)
        doc = parse(upper)
        assert doc.metadata["format"] == "txt"

    def test_string_path_is_accepted(self, sample_txt: Path) -> None:
        doc = parse(str(sample_txt))
        assert doc.metadata["format"] == "txt"

    def test_unknown_extension_raises(self, fixtures_dir: Path) -> None:
        p = fixtures_dir / "manual.docx"
        p.write_text("anything", encoding="utf-8")
        with pytest.raises(UnsupportedFormatError):
            parse(p)

    def test_no_extension_raises(self, fixtures_dir: Path) -> None:
        p = fixtures_dir / "noext"
        p.write_text("anything", encoding="utf-8")
        with pytest.raises(UnsupportedFormatError):
            parse(p)

    def test_unknown_extension_error_lists_supported(
        self, fixtures_dir: Path
    ) -> None:
        p = fixtures_dir / "x.docx"
        p.write_text("x", encoding="utf-8")
        with pytest.raises(UnsupportedFormatError) as excinfo:
            parse(p)
        msg = str(excinfo.value)
        # Should mention at least one of the supported extensions so
        # the user knows what's accepted.
        assert ".pdf" in msg or ".txt" in msg or ".md" in msg

    def test_unknown_extension_error_preserves_path(
        self, fixtures_dir: Path
    ) -> None:
        p = fixtures_dir / "x.docx"
        p.write_text("x", encoding="utf-8")
        with pytest.raises(UnsupportedFormatError) as excinfo:
            parse(p)
        assert excinfo.value.path == p


class TestTxtParser:
    """``TxtParser`` happy path + failure modes."""

    def test_happy_path_returns_text_and_metadata(
        self, sample_txt: Path
    ) -> None:
        doc = TxtParser().parse(sample_txt)
        assert "Living-Room Thermostat Manual" in doc.text
        assert doc.metadata["format"] == "txt"
        assert doc.metadata["source"] == "manual.txt"
        assert doc.metadata["page_count"] == 1
        assert doc.metadata["char_count"] == len(doc.text)

    def test_pages_is_single_element(self, sample_txt: Path) -> None:
        doc = TxtParser().parse(sample_txt)
        assert len(doc.pages) == 1
        assert doc.pages[0][0] == 1
        assert doc.pages[0][1] == doc.text

    def test_missing_file_raises_parser_error(
        self, fixtures_dir: Path
    ) -> None:
        with pytest.raises(ParserError):
            TxtParser().parse(fixtures_dir / "does_not_exist.txt")

    def test_empty_file_raises_empty_document_error(
        self, empty_txt: Path
    ) -> None:
        with pytest.raises(EmptyDocumentError):
            TxtParser().parse(empty_txt)

    def test_whitespace_only_file_raises_empty_document_error(
        self, whitespace_txt: Path
    ) -> None:
        with pytest.raises(EmptyDocumentError):
            TxtParser().parse(whitespace_txt)

    def test_bom_is_stripped(self, sample_txt_bom: Path) -> None:
        """UTF-8 BOM (\ufeff) at the start of a Windows-saved file is removed."""
        doc = TxtParser().parse(sample_txt_bom)
        # The BOM should not appear in the extracted text.
        assert "\ufeff" not in doc.text
        assert doc.text.startswith("Hello from Windows Notepad")

    def test_invalid_utf8_raises_unicode_decode_error(
        self, fixtures_dir: Path
    ) -> None:
        p = fixtures_dir / "latin1.txt"
        # 0xff is not a valid UTF-8 start byte; 0xa9 alone (in this
        # position) is also invalid. Together they are a valid
        # Windows-1252 copyright symbol but not valid UTF-8.
        p.write_bytes(b"\xff\xfehello\n")
        with pytest.raises(UnicodeDecodeError):
            TxtParser().parse(p)


