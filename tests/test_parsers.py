"""Tests for src/tinyrag/ingestion/parsers.py — document parsers.

Test layout
-----------
- TestPublicSurface     — every public name is exported from the
  subpackage (``tinyrag.ingestion.parse``, ``ParsedDocument``,
  ``ParserError``, ``DocumentParser``, the three concrete parsers).
- TestProtocolIsRuntime — :class:`DocumentParser` is
  ``@runtime_checkable``; all three concrete parsers satisfy it
  via duck-typing.
- TestParsedDocument    — the result dataclass is frozen; the
  ``pages`` default is empty (not mutable shared state).
- TestExtensionDispatch — :func:`parse` routes by extension
  (case-insensitive); raises :class:`UnsupportedFormatError` on
  unknown extensions.
- TestTxtParser         — happy path, missing file, empty file,
  whitespace-only file, UTF-8 BOM tolerance, exact contract.
- TestMarkdownParser    — happy path, front-matter stripped,
  image/link URL noise stripped, missing/empty files.
- TestPdfParser         — happy path (multi-page, page numbers
  preserved), missing file, empty PDF, malformed PDF.
- TestErrorHierarchy    — every public exception subclasses
  :class:`ParserError`; the right subclass is raised in each
  failure mode.

Why so many tests?
------------------
PDF parsing is famously finicky (the architecture doc calls this
out as 🟡 Standard review intensity). The failure modes — empty
files, encoded PDFs, scanned PDFs, missing front-matter, mixed
encoding — all need explicit coverage so a regression catches the
problem before the user does.

PDF fixture strategy
--------------------
No PDF writer library (``fpdf``, ``reportlab``, ``pypdf``) is in
our dependency tree. We construct minimal valid PDFs by hand using
PDF's text-based structure. A 2-page fixture is generated by
:func:`_build_minimal_pdf` — enough for ``pdfplumber`` to extract
text from each page.

Location: ``tests/test_parsers.py``
"""

