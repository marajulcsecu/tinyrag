# ----------------------------------------------------------------------------
# PDF fixture builder
# ----------------------------------------------------------------------------
#
# We construct a minimal valid PDF by hand. A PDF is a text-based
# container of objects; the minimum needed for pdfplumber to extract
# text is:
#
#   1. A catalog object (the PDF's root).
#   2. A pages object (the page tree root).
#   3. One or more page objects (each referencing a content stream
#      and a font resource).
#   4. A font object (we use the built-in Helvetica).
#   5. A content stream with the actual text-drawing operators.
#
# Each object is a numbered "X 0 obj ... endobj" block. The objects
# form a cross-reference table that pdfplumber walks to assemble the
# document. We rebuild the xref byte offsets dynamically after we
# know each object's final size.
#
# We use FlateDecode (zlib) compression on the content streams to
# keep the fixture small and to behave like a real-world PDF
# (most PDFs use FlateDecode for streams).


def _make_pdf_stream(text: str) -> bytes:
    """Build a single PDF content stream drawing ``text`` with Helvetica.

    The stream uses the ``Tj`` operator (show text). Newlines in
    the input become ``\\n`` in the rendered PDF (we don't draw
    actual line breaks; that's enough for our chunker).
    """
    # Escape PDF special characters in the text. PDF text strings
    # are wrapped in parentheses; we escape ``(`` and ``)`` and use
    # ``\\n`` for a literal newline.
    escaped = text.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
    content = f"BT /F1 12 Tf 50 750 Td ({escaped}) Tj ET"
    return zlib.compress(content.encode("latin-1"))


def _build_minimal_pdf(pages: list[str]) -> bytes:
    """Build a minimal valid PDF with one page per entry in ``pages``.

    Each page's content stream draws the corresponding string. The
    PDF is intentionally tiny — no images, no fancy fonts — but
    pdfplumber can extract the text on each page correctly.
    """
    # Object 1: catalog. 2: pages. 3: font. Then page pairs (page + content).
    # Object numbering:
    #   1: Catalog
    #   2: Pages
    #   3: Font (F1, Helvetica)
    #   4..N: alternating Page / Content objects (4, 5, 6, 7, ...)
    n_pages = len(pages)
    objects: list[bytes] = [
        # 1: Catalog -> Pages
        b"1 0 obj\n<< /Type /Catalog /Pages 2 0 R >>\nendobj\n",
        # 2: Pages -> each Page
        b"2 0 obj\n<< /Type /Pages /Kids ["
        + b" ".join(f"{4 + 2 * i} 0 R".encode() for i in range(n_pages))
        + b"] /Count "
        + str(n_pages).encode()
        + b" >>\nendobj\n",
        # 3: Font
        b"3 0 obj\n<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>\nendobj\n",
    ]
    for i, page_text in enumerate(pages):
        page_obj_num = 4 + 2 * i
        content_obj_num = page_obj_num + 1
        stream_data = _make_pdf_stream(page_text)
        # Page object references its content stream and the font.
        objects.append(
            f"{page_obj_num} 0 obj\n"
            f"<< /Type /Page /Parent 2 0 R "
            f"/MediaBox [0 0 612 792] "
            f"/Contents {content_obj_num} 0 R "
            f"/Resources << /Font << /F1 3 0 R >> >> >>\n"
            f"endobj\n".encode()
        )
        # Content stream.
        objects.append(
            f"{content_obj_num} 0 obj\n"
            f"<< /Length {len(stream_data)} /Filter /FlateDecode >>\n"
            f"stream\n".encode()
            + stream_data
            + b"\nendstream\nendobj\n"
        )

    # Header.
    header = b"%PDF-1.4\n%\xc2\xa5\xc2\xb1\xc2\xab\n"  # binary marker
    body = b"".join(objects)

    # Cross-reference table. ``pdfplumber`` (via pypdf) needs this to
    # locate each object. We record the byte offset of every object
    # and emit the classic ASCII xref table (each entry is exactly
    # 20 bytes: 10-digit offset + space + 5-digit gen + space + flag).
    offsets: list[int] = []
    cur = len(header)
    for obj in objects:
        offsets.append(cur)
        cur += len(obj)
    xref_offset = len(header) + len(body)
    xref_block = (
        b"xref\n"
        + str(len(objects) + 1).encode()
        + b" 0\n"
        + b"0000000000 65535 f \n"
    )
    for off in offsets:
        xref_block += f"{off:010d} 00000 n \n".encode()
    trailer = (
        b"trailer\n"
        b"<< /Size " + str(len(objects) + 1).encode() + b" /Root 1 0 R >>\n"
        b"startxref\n" + str(xref_offset).encode() + b"\n"
        b"%%EOF\n"
    )
    return header + body + xref_block + trailer


