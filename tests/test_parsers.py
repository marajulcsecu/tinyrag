class TestErrorHierarchy:
    """Every public exception inherits from :class:`ParserError`."""

    @pytest.mark.parametrize(
        "exc_cls",
        [
            UnsupportedFormatError,
            EmptyDocumentError,
            PdfReadError,
        ],
    )
    def test_subclass_of_parser_error(
        self, exc_cls: type[ParserError]
    ) -> None:
        assert issubclass(exc_cls, ParserError)

    def test_can_catch_all_with_parser_error(
        self, sample_pdf: Path
    ) -> None:
        """A single ``except ParserError`` catches every parser failure."""
        # We don't actually invoke parse here — we just demonstrate
        # the hierarchy by raising each subclass and catching via base.
        for exc_cls in (
            UnsupportedFormatError,
            EmptyDocumentError,
            PdfReadError,
        ):
            with pytest.raises(ParserError):
                raise exc_cls("test")


